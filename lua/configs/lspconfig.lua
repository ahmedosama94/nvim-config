local servers = {
  "html",
  "cssls",
  "eslint",
  "intelephense",
  "gopls",
  "volar",
  -- "phpstan",
}
local nvlsp = require "nvchad.configs.lspconfig"

-- lsps with default config
for _, lsp in ipairs(servers) do
  lspconfig[lsp].setup {
    on_attach = nvlsp.on_attach,
    on_init = nvlsp.on_init,
    capabilities = nvlsp.capabilities,
  }
end

